def get_lte_info():
    """Connects to router via SSH and runs commands."""
    try:
        # Router SSH details
        HOST = "192.168.103.1"
        USERNAME = "root"
        PASSWORD = ""  # Change to your actual password

        # Commands to execute
        COMMANDS = [
            "uqmi -d /dev/cdc-wdm0 --get-serving-cell",
            "uqmi -d /dev/cdc-wdm0 --get-signal-info"
        ]
        # Establish SSH connection
        ssh = paramiko.SSHClient()
        ssh.set_missing_host_key_policy(paramiko.AutoAddPolicy())
        ssh.connect(HOST, username=USERNAME, password=PASSWORD)

        for command in COMMANDS:
            print(f"Executing: {command}")
            stdin, stdout, stderr = ssh.exec_command(command)
            output = stdout.read().decode().strip()
            error = stderr.read().decode().strip()

            if output:
                print(f"‚úÖ Output:\n{output}")
            if error:
                print(f"‚ùå Error:\n{error}")

        ssh.close()
        return output, error
    except Exception as e:
        print(f"‚ùå SSH Connection Failed: {e}")

